#!/usr/bin/env node

import { Command } from 'commander'
import { confirm, input } from '@inquirer/prompts'
import degit from 'degit'
import fs from 'fs'
import path from 'path'
import { cp } from 'fs/promises'

// Define the CLI using commander
const program = new Command()

program
  .name('create-saas')
  .description('Scaffold a multi-feature SaaS application with selected integrations')
  .argument('[directory]', 'Target directory name')
  .option('-t, --template <template>', 'Template source (GitHub repo slug or local path)', 'github:gurunesh30/refactored-umbrella')
  .action(async (directoryArg, options) => {
    try {
      console.log('Welcome to the SaaS Setup Wizard!')
      console.log('---------------------------------\n')

      // 1. Prompt for directory if not provided
      let targetDir = directoryArg
      if (!targetDir) {
        targetDir = await input({
          message: 'What is the target directory name for your project?',
          default: 'my-saas-app',
          validate: (val) => val.trim().length > 0 || 'Directory name cannot be empty.'
        })
      }

      const targetPath = path.resolve(process.cwd(), targetDir)

      if (fs.existsSync(targetPath) && fs.readdirSync(targetPath).length > 0) {
        console.error(`[Error] Target directory "${targetDir}" already exists and is not empty.`)
        process.exit(1)
      }

      // 2. Select integrations one-by-one (reliable on all terminals)
      console.log('Select cloud integrations to configure:\n')

      const selectedIntegrations = []

      const useSupabase = await confirm({
        message: 'Include Supabase (Database, Auth, RLS)?',
        default: true
      })
      if (useSupabase) selectedIntegrations.push('supabase')

      const useStripe = await confirm({
        message: 'Include Stripe (Subscriptions, Billing Portal, Webhooks)?',
        default: true
      })
      if (useStripe) selectedIntegrations.push('stripe')

      const useVercel = await confirm({
        message: 'Include Vercel (Deployments, Project settings)?',
        default: false
      })
      if (useVercel) selectedIntegrations.push('vercel')

      if (selectedIntegrations.length === 0) {
        console.log('\n[Warning] No integrations selected. A minimal .env.local will be generated.')
      }

      console.log(`\n[Success] Selected: ${selectedIntegrations.length > 0 ? selectedIntegrations.join(', ') : 'none'}`)

      console.log(`\nScaffolding project into: ${targetPath}...`)

      // 3. Clone / Copy base template
      const templateSource = options.template === 'refactored-umbrella' 
        ? 'github:gurunesh30/refactored-umbrella' 
        : options.template

      const isLocalTemplate = fs.existsSync(templateSource) && fs.lstatSync(templateSource).isDirectory()

      if (isLocalTemplate) {
        console.log(`Copying local template from: ${templateSource}...`)
        // Create target directory first
        fs.mkdirSync(targetPath, { recursive: true })
        
        // Copy top-level items individually to avoid root-to-child cycle errors
        const entries = fs.readdirSync(templateSource)
        for (const entry of entries) {
          if (['node_modules', '.next', '.git', targetDir].includes(entry)) continue
          
          const srcPath = path.join(templateSource, entry)
          const destPath = path.join(targetPath, entry)
          
          await cp(srcPath, destPath, {
            recursive: true,
            filter: (src) => {
              const basename = path.basename(src)
              return !['node_modules', '.next', '.git'].includes(basename)
            }
          })
        }
      } else {
        console.log(`Downloading template from: ${templateSource}...`)
        const emitter = degit(templateSource, {
          cache: false,
          force: true
        })

        emitter.on('info', (info) => {
          console.log(`  ${info.message}`)
        })

        await emitter.clone(targetPath)
      }

      // 4. Update root package.json name
      const packageJsonPath = path.join(targetPath, 'package.json')
      if (fs.existsSync(packageJsonPath)) {
        const pkg = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'))
        pkg.name = path.basename(targetPath)
        fs.writeFileSync(packageJsonPath, JSON.stringify(pkg, null, 2), 'utf8')
        console.log(`Updated root package.json name to "${pkg.name}"`)
      }

      // 5. Generate .env.local file
      console.log('Compiling .env.local configuration...')
      let envContent = `# Environment configuration generated by create-saas\n\n`
      envContent += `NEXT_PUBLIC_SITE_URL="http://localhost:3000"\n\n`

      if (selectedIntegrations.includes('supabase')) {
        envContent += `# Supabase Configuration\n`
        envContent += `NEXT_PUBLIC_SUPABASE_URL=""\n`
        envContent += `NEXT_PUBLIC_SUPABASE_ANON_KEY=""\n`
        envContent += `SUPABASE_SERVICE_ROLE_KEY=""\n\n`
      }

      if (selectedIntegrations.includes('stripe')) {
        envContent += `# Stripe Configuration\n`
        envContent += `STRIPE_SECRET_KEY=""\n`
        envContent += `STRIPE_WEBHOOK_SECRET=""\n`
        envContent += `STRIPE_PRO_PRICE_ID=""\n`
        envContent += `STRIPE_CUSTOMER_ID=""\n\n`
      }

      if (selectedIntegrations.includes('vercel')) {
        envContent += `# Vercel Configuration\n`
        envContent += `VERCEL_PROJECT_ID=""\n`
        envContent += `VERCEL_ORG_ID=""\n\n`
      }

      // Write .env.local to target root and apps/web
      fs.writeFileSync(path.join(targetPath, '.env.local'), envContent, 'utf8')
      
      const webAppsPath = path.join(targetPath, 'apps', 'web')
      if (fs.existsSync(webAppsPath)) {
        fs.writeFileSync(path.join(webAppsPath, '.env.local'), envContent, 'utf8')
      }

      console.log('Setup completed successfully!\n')
      console.log('Next steps:')
      console.log(`  1. cd ${targetDir}`)
      console.log('  2. npm install')
      console.log('  3. Update .env.local with your cloud credentials')
      console.log('  4. npm run dev (runs web on port 3000 and diagnostics on port 4000)')
    } catch (err) {
      console.error('[Error] Setup failed:', err.message || err)
      process.exit(1)
    }
  })

program.parse(process.argv)
